def addieren(zahl1, zahl2):
    """Funktion zum Addieren"""
    summe = zahl1 + zahl2
    return summe


def subtrahieren(zahl1, zahl2):
    """Funktion zum Subtrahieren"""
    differenz = zahl1 - zahl2
    return differenz


def multiplizieren(zahl1, zahl2):
    """Funktion zum Multiplizieren"""
    produkt = zahl1 * zahl2
    return produkt


def dividieren(zahl1, zahl2):
    """Funktion zum Dividieren"""
    quotient = zahl1 / zahl2
    return quotient
